    // MÉTODO DE INICIO DE JUEGO
    private void iniciar(){
        this.panel_juego.removeAll();
        this.intentos.setText("Llevas "+contador+" intentos");
        int n_fil , n_col;
        contador = 0;
        
        n_fil = Integer.parseInt(this.in_row.getText());
        n_col = Integer.parseInt(this.in_col.getText());
        
        this.panel_juego.setLayout(new java.awt.GridLayout(n_fil, n_col));
        
        
        this.num_cuadros = n_fil * n_col;
        this.contador_minas = 0;
        
        for (int i = 0; i < n_fil ; i++){
            for (int j = 0; j < n_col; j++){
                Cuadro temp = new Cuadro(); // crea cada celda
                temp.addActionListener(this); // todos los botones que se creen los pone a la espera de ser pulsado
                if (temp.esta_minado())
                    this.contador_minas++;
                temp.setVisible(true);
                this.panel_juego.add(temp); // añade la celda al panel de juego
            }
        }
    }


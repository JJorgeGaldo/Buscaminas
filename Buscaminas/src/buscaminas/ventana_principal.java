    public void actionPerformed (ActionEvent e){
        Cuadro temp2 = (Cuadro)e.getSource(); // con getSource sabes cuál de todos los botones ha sido presionado y lo asignas a la variable temp2
        if (temp2.esta_minado()){
            this.setBackground(java.awt.Color.RED);
            javax.swing.JOptionPane.showMessageDialog(this, "Pulsaste sobre una mina.. Perdiste");
            this.contador = 0;
        }else{
            temp2.setBackground(java.awt.Color.PINK);
            contador ++;
            intentos.setText("Llevas "+contador + " intentos");
            if (num_cuadros - contador_minas == contador){
                javax.swing.JOptionPane.showMessageDialog(this, "GANASTE!!");
            }
               
        }
        
    }

